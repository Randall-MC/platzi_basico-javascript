let fruits = ["Apple", "Banana", "Cherry", "Strawberry"];
// How many elements are there
fruits.length;
// Access to an element of the array
fruits[1];
// Add a new element at the end of the array
fruits.push("Grape");
// Delete the last item in the array
fruits.pop();
// Add an element at the start of the array
fruits.unshift("Grapes");
// Delete the first element of the array
fruits.shift();
// Know the position of an element
fruits.indexOf("Cherry");